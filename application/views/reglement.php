<h3>REGLEMENT DE LA BOURSE AUX VELOS SAINT NAZAIRE <?php echo @date('Y'); ?></h3>
<dl>
	<dt>Article 1 : champ d'application</dt>
	<dd>L'association AVS Saint Nazaire organise les <b>9, 10 et 11 novembre 2018</b> une Bourse aux Vélos (15 ème édition) exclusivement réservée aux vélos et gros matériels d'occasion <u>entre particuliers</u>. Cette Bourse aux vélos a lieu à <b>La Soucoupe avenue Léo Lagrange 44600 SAINT NAZAIRE</b></dd>
</dl>


<dl>
	<dt>Article 2 : dépôt des vélos</dt>
	<dd>Le dépôt est permanent le vendredi 9 novembre 2018 de 18h00 à 21h00, le samedi 10 novembre 2018 entre 8h00 et 19h00 et le dimanche 11novembre 2018 de 8h00 à 14h00.<br />
Une participation aux frais de dépôt est exigée pour la valeur de <b>3 euros</b> par vélo déposé.<br /> 
Le vendeur s'engage sur l'honneur sur la licité de l'origine des articles déposés, il devra présenter une pièce d'identité au moment du dépôt et ses coordonnées pourront être communiquées à l'acheteur s'il en fait la demande. </br />
Seuls les vélos en parfait état de fonctionnement, exempts de tout vice ou défaut qui les rendraient dangereux ou impropres à l'usage auquel ils sont destinés, sont acceptés.</dd>
</dl>


<dl>
	<dt>Article 3 : vente</dt>
	<dd>La vente se fera librement les 10 et 11 novembre 2018 de 8h00 à 19h00. Des adhérents de l'association AVS Saint Nazaire seront présents afin de conseiller les personnes qui le souhaitent. L'association AVS Saint Nazaire ne garantit pas la vente des vélos que vous nous confierez dans le cadre de la bourse aux vélos, mais s'engage à tout mettre en œuvre pour la faciliter. La vente est ferme et définitive.</dd>
</dl>


<dl>
	<dt>Article 4 : paiement du matériel vendu</dt>
	<dd>Le paiement du matériel se fera à partir du samedi 10 14H00 et le dimanche 11 novembre 2018 toute la journée jusqu’à 19H00. <br />
<b>Une commission de 10% </b> de la valeur de vente sera  demandée au vendeur au bénéfice de l'AVS Saint-Nazaire. Cette commission sera limitée à 100 € même si le vélo a été vendu à plus de 1000 €. La restitution du paiement ne pourra se faire que contre présentation du <b>reçu vendeur.</b></dd>
</dl>

<dl>
	<dt>Article 5 : paiement par correspondance</dt>
	<dd>Si la vente est effective, le paiement au vendeur pourra être <b>réalisé par correspondance</b> <u>pour les personnes habitant à plus de 30 km de St Nazaire</u>. Dans ce cas, le vendeur devra faire parvenir à la Bourse aux vélos : les 10% (Au moment du dépôt, il devra demander l’adresse de la BAV). Dans le courrier envoyé, il devra fournir en plus des 10%, une enveloppe timbrée à son adresse et le reçu vendeur de couleur. Dès réception, le montant de la vente lui sera envoyé. (Transaction souhaitable dans la semaine qui suit la Bourse aux vélos).</dd>
</dl>


<dl>
	<dt>Article 6 : responsabilité financière</dt>
	<dd>L’AVS dégage toute responsabilité sur la non-solvabilité de l’acheteur. La présentation d’une pièce d’identité sera demandée systématiquement en cas de règlement par chèque (deux pièces d’identité pour une vente de plus de 1000 €). En cas de litige, un double de la fiche dépôt sera adressé au vendeur.</dd>
</dl>


<dl>
	<dt>Article 7 : retrait des invendus</dt>
	<dd>Le retrait des invendus se fera uniquement le dimanche 11 novembre 2018 entre 17H00 et 19H00. La restitution du matériel et des vélos ne pourra se faire que contre présentation du <b>reçu vendeur</b>. Les vélos et matériels non retirés seront acquis à l'association AVS Saint Nazaire qui les redonnera à une association caritative de son choix.</dd>
</dl>


<dl>
	<dt>Article 8 : responsabilité</dt>
	<dd>L'AVS Saint Nazaire ne pourra en aucun cas être tenue responsable en cas d'accident survenant du fait du mauvais état d'un matériel ou vélo vendu en l’état par son intermédiaire même suite au réglage réalisé à l’atelier du site BAV.</dd>
</dl>

<dl>
	<dt>Article 9 : dégâts sur un vélo</dt>
	<dd>L’AVS ne pourra être rendu responsable de dégradations intervenues lors de la manifestation. En cas de mise en vente d’un vélo haut de gamme, <u>à la demande du propriétaire, sur les très beaux vélos</u>, il pourra être établi entre les deux parties un « état descriptif contradictoire » du vélo déposé. Ce « document » sera visé pour la partie AVS par le Président de l’AVS. En cas de litige, seul cet « état descriptif contradictoire » fera fois et s’il n’a pas été effectué, c’est le début de cet article qui prévaudra. Quoi qu’il en soit, l’AVS met tout en œuvre pour assurer la restitution des vélos dans le même état que déposé.</dd>
</dl>

<dl>
	<dt>Article 10 : acceptation du règlement</dt>
	<dd>Le dépôt  ou l’achat d’un vélo à la bourse aux vélos de St Nazaire vaut acceptation pleine et entière du présent règlement.</dd>
</dl>